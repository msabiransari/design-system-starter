#!/usr/bin/env node
/**
 * Generates examples/shared/theme-previews.js — a static map of each theme's
 * private (--_*) variables, expressed as an inline-style string.
 *
 * Why: the example overview gallery renders a live mini-preview per theme by
 * applying that theme's --_* values to a scoped element. It used to read the
 * theme CSS at runtime with fetch(), which browsers BLOCK under the file://
 * protocol (CORS, origin "null"). Baking the values into a committed JS file
 * makes the gallery work when the page is opened directly from disk AND over
 * http, with no async timing. Themes remain the single source of truth — this
 * file is regenerated from themes/*.css (wired into `npm run build`).
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const THEMES_DIR = path.join(ROOT, 'themes');
const OUT = path.join(ROOT, 'examples', 'shared', 'theme-previews.js');

function extractVars(cssText) {
  const vars = {};
  const regex = /--_([a-z0-9-]+):\s*([^;]+);/g;
  let m;
  while ((m = regex.exec(cssText)) !== null) {
    vars[m[1]] = m[2].trim();
  }
  return vars;
}

function varsToStyle(vars) {
  return Object.entries(vars)
    .map(([k, v]) => `--_${k}: ${v};`)
    .join(' ');
}

// The `auto` theme carries light values in the default block and dark values
// inside @media (prefers-color-scheme: dark); split them for a side-by-side preview.
function lightBlock(cssText) {
  const i = cssText.indexOf('@media');
  return i >= 0 ? cssText.slice(0, i) : cssText;
}
function darkBlock(cssText) {
  const m = cssText.match(/@media\s*\(\s*prefers-color-scheme\s*:\s*dark\s*\)\s*\{([\s\S]*)\}/);
  return m ? m[1] : '';
}

const previews = {};
for (const file of fs.readdirSync(THEMES_DIR).sort()) {
  if (!file.endsWith('.css')) continue;
  const id = file.replace(/\.css$/, '');
  const css = fs.readFileSync(path.join(THEMES_DIR, file), 'utf8');
  if (id === 'auto') {
    previews[id] = {
      light: varsToStyle(extractVars(lightBlock(css))),
      dark: varsToStyle(extractVars(darkBlock(css))),
    };
  } else {
    previews[id] = varsToStyle(extractVars(css));
  }
}

const banner = '/* AUTO-GENERATED by scripts/generate-theme-previews.js — do not edit by hand. */\n';
fs.writeFileSync(OUT, banner + 'window.DS_THEME_PREVIEWS = ' + JSON.stringify(previews, null, 2) + ';\n');
console.log('Wrote ' + path.relative(ROOT, OUT) + ' (' + Object.keys(previews).length + ' themes)');
