#!/usr/bin/env node
/**
 * Generates examples/shared/icon-sprite.js — a script that injects the SVG icon
 * sprite (icons/sprite.svg) inline into the page.
 *
 * Why: the examples reference icons with <use href="...sprite.svg#id">. Browsers
 * BLOCK external <use> references under the file:// protocol (same security
 * reason that blocks fetch()), so icons vanish when a page is opened directly
 * from disk. Injecting the sprite inline lets pages reference it with a local
 * fragment (<use href="#id">), which works under file:// AND http. The sprite
 * stays the single source of truth — this file is regenerated from
 * icons/sprite.svg (wired into `npm run build`).
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'icons', 'sprite.svg');
const OUT = path.join(ROOT, 'examples', 'shared', 'icon-sprite.js');

const svg = fs.readFileSync(SRC, 'utf8').trim();

const banner = '/* AUTO-GENERATED by scripts/generate-icon-sprite.js — do not edit by hand. */\n';
const body = `(function () {
  var SPRITE = ${JSON.stringify(svg)};
  function inject() {
    if (document.getElementById('ds-icon-sprite-host')) return;
    var host = document.createElement('div');
    host.id = 'ds-icon-sprite-host';
    host.setAttribute('aria-hidden', 'true');
    host.style.display = 'none';
    host.innerHTML = SPRITE;
    document.body.insertBefore(host, document.body.firstChild);
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', inject);
  } else {
    inject();
  }
})();
`;

fs.writeFileSync(OUT, banner + body);
console.log('Wrote ' + path.relative(ROOT, OUT));
